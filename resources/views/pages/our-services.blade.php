@section('inline-scripts')
jQuery(function() {
// Function to display the content based on URL slug
function showContentFromUrl() {
var urlParams = new URLSearchParams(window.location.search);
var service = urlParams.get('service');
if (service) {
$('.services-content').hide();
$('#' + service).show();
$('li[data-service="' + service + '"]').addClass('active');
$('html, body').animate({
scrollTop: $('#' + service).offset().top
}, 1000);
} else {
// Show the first service content on page load if no service is specified in the URL
var firstService = $('.service').first().data('service');
$('#' + firstService).show();
$('.service').first().addClass('active');
}
}

// Initially show content based on URL or the first service content
showContentFromUrl();

// Handle service click
$('.service').on('click', function(e) {
e.preventDefault();
var service = $(this).data('service');
var slug = $(this).data('slug');
$('.services-content').hide();
$('#' + service).show();
$('.service').removeClass('active');
$(this).addClass('active');
history.pushState({}, '', '?service=' + slug);
});
});
